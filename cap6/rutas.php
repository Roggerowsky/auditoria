<br>
<br>El acceso a la computadora no significa tener una entrada sin restricciones. Limitar el acceso solo a los niveles apropiados puede proporcionar una mayor seguridad.
<br>El objetivo de la seguridad de los sistemas de información es controlas las operaciones y su ambiente mediante el monitoreo del acceso a la información y los programas para poder darle seguimiento y determinar la causa probable de desviaciones. Por ello es conveniente al utilizar algún tipo de software dentro de un sistema, contar con una ruta de acceso.
<br>Cada uno de los sistemas de información tiene una ruta de acceso la cual puede definirse como la trayectoria seguida en el momento de acceso al sistema.
<br>Como se ha señalad, un usuario puede pasar por uno o por múltiples niveles de seguridad antes de obtener acceso a los programas y datos. Los tipos de restricciones de acceso son:
<li>•	Solo lectura 
<li>•	Solo consulta 
<li>•	Lectura y consulta 
<li>•	Lectura y escritura, para crear, actualizar, borrar, ejecutar o copiar
<br>El esquema identifica  a los usuarios del sistema, los tipos de dispositivos por los cuales se accesa al programa, el software usado para el acceso al sistema, los recursos que pueden ser accesados y los sistemas donde residen estos recursos. Los sistemas pueden se en línea, fuera de línea, batch y rutas de telecomunicación.
<br>El esquema de las rutas de acceso sirve para identificar todos los puntos de control que pueden ser usados para proteger los datos ene l sistema. El auditor debe conocer las rutas de acceso para la evaluación de los puntos de control apropiados 

