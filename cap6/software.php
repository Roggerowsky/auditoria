<br>Este puede ser definido como el software diseñado para permitir el manejo y control del acceso a los siguientes recursos:
<li>•	Programas de librerías 
<li>•	Archivos de datos.
<li>•	Jobs.
<li>•	Programas de aplicación.
<li>•	Módulos de funciones.
<li>•	Utilerías.
<li>•	Diccionario de datos
<li>•	Archivos 
<li>•	Programas
<li>•	Comunicación 
<br>Controla el acceso a la información, grabando e investigando los eventos realizados y el acceso  a los recursos, por medio de la identificación del usuario. El software de control de acceso, tiene las siguientes funciones:
<li>•	Definición de usuarios
<li>•	Definiciones de las funciones del usuario después de accesar el sistema 
<li>•	Establecimiento de auditoria  a través del uso del sistema
<br>El software de seguridad protege los recursos mediante la identificación de los usuarios autorizados con la llave de acceso, que son archivadas y guardadas por este software.
<br>Esto puede ser efectuado a través de la creación de archivos o tablas de seguridad. Lo0s paquetes de seguridad frecuentemente incluyen facilidades para encriptar estas tablas o archivos.
<br>A cada usuario se le debe de asignar un alcance en el acceso y por cada recurso un grado de protección, para que los recursos puedan ser protegidos de un  acceso no autorizado.
<br>Algunos paquetes de seguridad pueden ser usados para restringir el acceso a programas, librerías y archivos de datos; otros pueden además limitar el uso de terminales o restringir el acceso a bases de datos, y existen otros más para confirmar y evaluar la autorización de la terminal remota para utilizar determinada información. Estos pueden variar en el nivel de la seguridad brindada a los archivos de datos. La seguridad puede estar basada en el tipo de acceso de los usuarios autorizados para agregar registros a un archivo a loa que únicamente leen registros.
<br>La mayor ventaja del software de seguridad es la capacidad  para proteger los recursos de accesos  no autorizados, incluyendo los siguientes:
<li>•	Procesos en espera de modificación por un programa de aplicación 
<li>•	Accesos por los editores en línea
<li>•	Accesos por utilerías de software
<li>•	Accesos  a archivos de las bases de datos, a través de un manejador de base de datos(DBMS)
<li>•	Acceso de terminales o estaciones no autorizadas
<br>Estos paquetes pueden restringir el acceso a los recursos (archivos de datos), reduciendo asi el riesgo de los accesos no autorizados
<br>Otra característica de estos paquetes es que se pueden detectar las violaciones de seguridad, tomando las siguientes medidas:
<li>•	Terminaciones de procesos 
<li>•	Forzara a las terminales a apagarse 
<li>•	Desplegar mensajes de error
<li>•	Escribir los registros para la auditoria 
<br>La bitácora de auditoria es seleccionada durante la implementación. 
<br>Cada bitácora debe de incluir la identificación del usuario: si el acceso es exitoso, deben considerarse los recursos accesados, dia, hora, terminal y un dato específico de lo que fue modificado durante el acceso; si el acceso no fue exitoso la mayor informacion posible sobre dia, hora, terminal y claves de intento usadas.
