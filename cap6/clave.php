<br>
Un área importante en la seguridad lógica es el control de las claves de acceso de los usuarios. Existen deferentes métodos de identificación para el usuario:
<li>•	Un password o código
<li>•	Una credencial con banda magnética
<li>•	Algo específico de usuario (características propias)
<br><br>La identificación  es definida como el proceso de distinción de un usuario de otros. La identificación de entrada proporcionara un reconocimiento individual, cada usuario debe de tener una identificación de entrada única que debe ser reconocida por el sistema.
<br>Password, código o llaves de acceso. La identificación de los individuos es usualmente conocida y está asociada con un password  o clave de acceso. Las claves de acceso pueden ser usadas para controlar el acceso a la computadora, a sus recursos, asi como definir nivel de acceso o funciones específicas. Las llaves de acceso deben tener las siguientes características:
<li>•	El sistema debe de verificar primero que el usuario tenga una llave de acceso valida
<li>•	La llave de acceso debe ser de una longitud adecuada para ser un secreto
<li>•	La llave de acceso no debe ser desplegada cuando es tecleada.
<li>•	Las llaves de acceso deben ser encriptados, ya que esto reduce el riesgo de que alguien obtenga la llave de acceso de otras personas 
<li>•	Las llaves de acceso deben de prohibir el uso de nombres, palabras o cadenas, de caracteres difíciles de retener, además el password no debe de ser cambiado por un valor pasado. Se recomienda la combinación de caracteres alfabéticos numéricos. No debe ser particularmente idenficable con el usuario, con su nombre, apellido o fecha de nacimiento.
<br><br>Credenciales con banda magnética. La banda magnética de las credenciales es frecuentemente usada para la entrada al sistema. Esta credencial es como una bancaria pero se recomienda que tenga  fotografía y firma.
<br>La ventaja más importante de la credencial es prevenir la estrada de impostores al sistema. Una credencial ordinaria  es fácil de falsificar, por lo que se debe elaborar de una manera especial, que no permite que sea reproducida.
<br>Validación por características. Es un método para la identificación del usuario, que es implantado con tecnología biométrica. Consiste en la verificación y reconocimiento de la identidad de las personas, basándose en características propias. Algunos de los dispositivos biométricos son:
<li>•	Las huellas dactilares
<li>•	La retina 
<li>•	La geometría de la mano 
<li>•	La firma 
<li>•	La voz

