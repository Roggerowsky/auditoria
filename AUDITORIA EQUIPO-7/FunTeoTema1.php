<center><h1>TECNICAS PARA LA INTERPRETACION DE LA INFORMACION </h1></center>
Tecnicas para la interpretacion de la informacion
El analisis critico de los hechos sirve para discriminar y evaluar la informacion, se basa en
las siguientes preguntas:<br>
Pregunta finalidad que determina<br><p>
Que, ...........el proposito <br>
Donde, .......el lugar<br>
Cuando, .....el orden y el momento<br>
Quien, ........la persona responsable<br>
Como, ........los medios<br>
Cuanto, ......la cantidad<br><p>
Las respuestas deben ser sometidas a la pregunta Por que para justificar la informacion
obtenida, cada interrogante  debe descomponer de la siguiente manera:<p>


<ol>
<li>Proposito
(a) Que se hace<br>
(b) Por que se hace<br>
(c) Que otra cosa podria hacerse<br>
(d) Que deberia hacerse<br><p>
<li>Lugar
(a) Donde se hace<br>
(b) Por que se hace ahi<br>
(c) En que otro lugar podria hacerse<br>
(d) Donde deberia hacerse<br><p>
<li>Sucesion<br>
(a) Cuando se hace<br>
(b) Por que se hace entonces<br>
(c) Cuando podria hacerse<br>
(d) Cuando deberia hacerse<br><p>
<li>Persona<br>
(a) Quien lo hace<br>
(b) Por que lo hace esa persona<br>
(c) Que otra persona podria hacerlo<br>
(d) Quien deberia hacerlo<br><p>
<li>Medios
(a) Como se hace<br>
(b) Por que se hace de ese modo<br>
(c) De que otro modo podria hacerse<br>
(d) Como deberia hacerse<br><p>
<li>Cantidad<br>
(a) Cuanto se hace<br>
(b) Por que se hace esa cantidad (volumen)<br>
(c) Cuanto podria hacerse<br>
(d) Cuanto deberia hacerse<br><p>
</ol>

