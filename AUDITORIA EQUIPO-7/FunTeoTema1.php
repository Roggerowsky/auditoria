Para interpretar  la información se puede utilizar desde técnicas muy sencillas hasta técnicas complejas de auditoria.

<h2>ANÁLISIS CRÍTICO DE LOS HECHOS</h2>

Una de las primeras técnicas es el análisis crítico de los hechos. Esta técnica 
sirve para discriminar y evaluar la infromacion que se basa en la aplicación de las siguienes preguntas:<br>
Pregunta----------finalidad que determina</br>
Qué, ..................el propósito <br>
Dónde, ..............el lugar<br>
Cuándo, ............el orden y el momento<br>
Quién, ...............la persona responsable<br>
Cómo, ...............los medios<br>
Cuánto, .............la cantidad<br><p>

La pregunta más importante es que, pues la respues permitirá saber si puede ser:
<ul>
<li>Eliminada</li>
<li>Modificada o cambiada</li>
<li>Simplificada</li>
</ul>

Las respuestas deben ser sometidas a la pregunta Por que para justificar la informacion
obtenida, cada interrogante  debe descomponer de la siguiente manera:<p>


<ol>
<li>Proposito<br>
(a) Que se hace<br>
(b) Por que se hace<br>
(c) Que otra cosa podria hacerse<br>
(d) Que deberia hacerse<br><p>
<li>Lugar<br>
(a) Donde se hace<br>
(b) Por que se hace ahi<br>
(c) En que otro lugar podria hacerse<br>
(d) Donde deberia hacerse<br><p>
<li>Sucesion<br>
(a) Cuando se hace<br>
(b) Por que se hace entonces<br>
(c) Cuando podria hacerse<br>
(d) Cuando deberia hacerse<br><p>
<li>Persona<br>
(a) Quien lo hace<br>
(b) Por que lo hace esa persona<br>
(c) Que otra persona podria hacerlo<br>
(d) Quien deberia hacerlo<br><p>
<li>Medios
(a) Como se hace<br>
(b) Por que se hace de ese modo<br>
(c) De que otro modo podria hacerse<br>
(d) Como deberia hacerse<br><p>
<li>Cantidad<br>
(a) Cuanto se hace<br>
(b) Por que se hace esa cantidad (volumen)<br>
(c) Cuanto podria hacerse<br>
(d) Cuanto deberia hacerse<br><p>
</ol>

<h2>METODOLOGÍA PARA OBTENER EL GRADO DE MADUREZ DEL SISTEMA</h2>
Para poder interpretar la infromacion de los sistemas se debe de evaluar el grado de madurez de los mismos:
<ul>
<li>Verificar si el sistema esta definido.</li>
<li>Verificar si el sistema esta estructurado.</li>
<li>Verificar si el sistema es realmente estable,</li>
<li>Verificar si los resultados son utilizados o no.</li>
</ul>

<h3>Caacteristicas</h3>
<ul>
<li>Definido
<li>Estructurado
<li>Estable 
<li>Resulados       
</ul>

<h3>maduro</h3>
<ul>
<li>completamente
<li>alta 
<li>no cambia
<li>utilizados 
</ul>

<h3>inmaduro</h3>
<ul>
<li>incompleto
<li>baja
<li>muchos cambios
<li>no utilizadps
</ul>

<p>Dependiendo del grado de madurez y de su grado de estructuración se determina si debe estar automatizado y la posible 
madurez que repercutirá en una mejor utilización y en disminución de cambios.</p>
<p>Si el sistema esta estructurado y maduro se debio usar la técnica de sistema de informacion, si esta estruturado pero 
no esta maduro se debio seguir  haciendo manualmente, si esta semiestructurado y maduro se podrá usar la técnica de 
soporte en la toma de las desiciones (DSS).</p>

                                  

