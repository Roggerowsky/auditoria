<center><h2>Evaluacion de los sistemas</h2></center>
Se debe evaluar la informaccion obtenida en los sistemas para poder:
<Ul type = disk >
<li>Determinar el objetivo y compararlo con lo obtenido
<li>Buscar la interrelaci´on con otros sistemas
<li>Evaluar la secuencia y flujo de las interacciones<br><p>

Para esto se debe:
<ol>
<li>Evalue el objetivo
Evalueel objetivo general y el alcance del sistema funcional en forma clara y precisa
<li>Evaluela interaccion con otros sistemas
Se debio analizas la informacion con el proposito de encontrar las interacciones y sus
contactos con otros sistemas a fin de determinar si existe un sistema integral de informacion,
sistemas asilados, o simplemente programas o si existe redundancia o ruido y
si existen controles.
<li>Evalue si se obtiene la secuencia y flujo de las interacciones
Es necesario establecer el flujo de informacion a traves del sistema graficandolo de tal
manera que refleje un periodo de tiempo asi como el orden de ocurrencia.
<li>Evalue el sistema funcional
Verificar la congruencia del objetivo.
<li>Evalue la modularidad del sistema
Ver que el objetivo particular de cada modulo es buscar el objetivo general del sistema.
<li>Evalue la segmentacion del sistema
Deben evaluarse aquellas funciones que son realizadas para distintos modulos, cada
funcion extraida del modulo debio ser consistente y validad con el usuario.
<li>Evalue la fragmentacion del sistema
Se deben evaluar las funciones haciendo enfasis en "que hace" y no en como lo hace.
<li>Evalue el flujo de informacion del sistema funcional
Identifique en cada documento su origen y su seguimiento a traves de las diferentes
entidades o departamentos por donde transita.
<li>Evalue los documentos de entrada y el contenido de los reportes
Verificar que los reportes o pantallas de salida contengan todos loas datos necesarios
sin importar de donde provienen.
<li>Evalue los controles de operacion del sistema
Ver en el proceso operacional si se llevan a cabo controles, es decir se evalua su razon
de ser, su metodo y su grado de sensibilidad.
<li>Cuantifique el volumen de informacion que se manejara
Debe tener una idea en la aproximacion de los recursos que se necesitan si estan siendo
usados correctamente.
<li>Evaluacion de los archivos
Senalar los atributos, propiedades, estructura, clasificacion, organizacion, factor de
bloque, frecuencia de uso, campos, codigos, tamanos de los archivos.
<li>Evaluacion de reportes
Se evaluaran la infraestructura de salida de reportes, por ejemplo los programas que
lo generan, los archivos usados, frecuencia. Usuario y contenido.
</ol>

